            var msg = '';
            var btn = $("button[type='submit']");
            var unset_list = $("#resumeUploadForm #email, #resumeUploadForm #phone, #resumeUploadForm #last_name, #resumeUploadForm #first_name, #resumeUploadForm #message");

            var email = $(this).find("#email").val();
            var phone = $(this).find("#phone").val();
            var first_name = $(this).find("#first_name").val();
            var last_name = $(this).find("#last_name").val();
            var message = $(this).find("#message").val();

            if (first_name == '') {
                msg = 'First Name Missing';
            } else if (last_name == '') {
                msg = 'Last Name Missing';
            }else if (email == '') {
                msg = 'Email Missing';
            } else if (phone == '') {
                msg = 'Phone Missing';
            } else if (message == '') {
                msg = 'Message Missing';
            }
            if (msg != '') {
                alert("Error: " + msg);
                return false;
            }
            btn.prop("disabled", true);
            btn.text("Waiting...");

            var formData = new FormData(this);

            $.ajax({
                type:'POST',
                url: $(this).attr('action'),
                data:formData,
                cache:false,
                contentType: false,
                processData: false,
                success:function(data){
                    alert(data.trim());
                    $("#message_show").click();
                    unset_list.val('');
                    btn.prop("disabled", false);
                    btn.text("Apply Now");
                    $("#message_show").remove();
                },
                error: function(data){
                    alert(data.trim());
                }
            });
        }));
